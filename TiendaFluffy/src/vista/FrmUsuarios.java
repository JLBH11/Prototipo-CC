    private void cargarUsuarios() {
        ArrayList<Usuario> lista = UsuarioDatos.obtenerUsuarios();
        for (Usuario u : lista) {
            Object[] fila = {
                u.getNombres(),
                u.getEmail(),
                u.getUsername(),
            };
            modeloTabla.addRow(fila);
        }
    }
    
